using System.IO;

namespace ClientCommon
{
	//=====================================================================================================================
	// (CommandBody 상속) 계정로그인 명령 데이터를 담고 있는 클래스
	//=====================================================================================================================
	public class LoginCommandBody : CommandBody
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Member variables

		// 인증서버로 부터 발급받은 액세스토큰
		public string accessToken;

		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Member functions

		//=====================================================================================================================
		// 직렬화 처리를 하는 함수
		//
		// writer : 직렬화 하여 내부 스트림에 저장하는 객체
		//=====================================================================================================================
		public override void Serialize(PacketWriter writer)
		{
			base.Serialize(writer);

			writer.Write(accessToken);
		}

		//=====================================================================================================================
		// 역직렬화 처리를 하는 함수
		//
		// reader : 내부 스트림에서 역직렬화 하여 다시 사용할 수 있는 데이터로 변환하는 객체
		//=====================================================================================================================
		public override void Deserialize(PacketReader reader)
		{
			base.Deserialize(reader);

			accessToken = reader.ReadString();
		}
	}

	//=====================================================================================================================
	// (ResponseBody 상속) 계정로그인 명령의 응답 데이터를 담고 있는 클래스
	//=====================================================================================================================
	public class LoginResponseBody : ResponseBody
	{
	}
}
