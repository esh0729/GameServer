using System.IO;

namespace ClientCommon
{
	public struct PDVector3
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Member variables

		public float x;
		public float y;
		public float z;

		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Constructors

		public PDVector3(float fX, float fY, float fZ)
		{
			x = fX;
			y = fY;
			z = fZ;
		}
	}
}
